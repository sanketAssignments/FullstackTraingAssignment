package oopquestion2;

	public class MyCrud 
	{
		 private int X;
		    private int Y;

		    public MyCrud() {
		        this(0,0);
		    }        
		    public MyCrud(int X, int Y) {
		        this.X = X;
		        this.Y = Y;
		    }
		    public int getX() {
		        return X;
		    }
		    public int getY() {
		        return Y;
		    }
		    public void setX(int X) {
		        this.X = X;
		    }
		    public void setY(int Y) {
		        this.Y = Y;
		    }
	
}
